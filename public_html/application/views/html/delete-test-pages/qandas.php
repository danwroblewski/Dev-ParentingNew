<h2>Question.<h2>
<p>My daughter is 7. She seems to be embarrassed to talk to me about things.<br />She also steals things I tell her she can’t have (like candy) and hides them.</p>
</div>

<img src="/img/printtray_blurtop.png" width="100%" />


<h2>Answer.</h2>
<p>Dealing with these issues when your daughter is 7 sets the stage for healthier behavior and a stronger relationship when she’s 15. We have a few tips for opening up the lines of communication so the two of you can discuss important topics. Also, we have some suggestions about how to teach your daughter not to steal and to respect your decisions about what she can and can’t have.</p>

<p><strong>Tip #1</strong> – Talk with Your Daughter Often. Spend a lot of time with your daughter around home and talk with her about a variety of things, including foods that are healthy, how to keep her room picked up, and how to respond to others when they make negative comments to her. Talk to her about making good choices and how her choices often determine whether good things or bad things happen to her. Talk to her about the importance of good personal hygiene. Be sure you have serious talks about having appropriate boundaries and not allowing others to see or touch her private parts or to show their private parts to her. When you can mix serious topics with casual topics, they are less scary for children and it is easier to have meaningful discussions.</p>

<p><strong>Tip #2</strong> – Have a Conversation About Not Stealing. Have a conversation about stealing with your daughter. Explain why it’s wrong and connect it to something she is familiar with, such as a movie or story where a "sneaky" character takes something that doesn’t belong to him. Talk about how others are uncomfortable around this sneaky character and how no one can trust him, especially when he’s around their valuable belongings. Then, let her know that taking candy (or anything else) that doesn’t belong to her or that she’s not supposed have is sneaky behavior, and that people won’t trust her if she keeps doing it. Also, be sure to give your daughter a negative consequence when she steals so she learns you won’t tolerate such behavior.</p>  

<p><strong>Tip #3</strong> – Teach Your Daughter to Always Ask Permission When She Wants Something. This should become a routine behavior. When your daughter asks permission to do or have something, and you say “No," she should learn to respond by saying "Okay" and accepting your answer. Explain to her that by using this skill, she won’t be a "sneaky character" and you will try to say "Yes" more often.</p>

