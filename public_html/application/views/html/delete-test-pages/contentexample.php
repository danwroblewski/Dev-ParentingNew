			<p>Parents have feared technology since the Stone Age. Plato was afraid that the written word would make people forgetful. Today, parents are concerned that too much time in front of a computer screen will stunt their children’s growth, maturity and social development.</p>

<p>The first step in helping your children interact with computers in a safe, healthy way is to embrace technology rather than fearing it. Develop a computer usage policy that reflects your family values and understanding of your child’s maturity level and needs. </p>

<p>As a parent, you are your child’s first and best teacher. Show your children how to tweet, blog, chat and use the computer in a safe, healthy way by setting clear expectations about where, when and what they can do online in your home. </p>

<h3>Where</h3>
<p>Establish technology-free zones in your home. Decide and discuss with your children where they can use the computer. Place the computer in the most public room in your home and restrict usage to that location. Discuss where the computer shouldn’t be used, such as at the dinner table or in the child’s bedroom.</p>

<h3>When</h3>
<p>Equally important to where is when your children can use the computer. Software programs such as getnetwise.org offer tools that help you monitor what your children do online. These programs allow you to turn off your child’s access to the Internet and email during certain times of the day or night.</p> 

<h3>What</h3>
<p>Finally and most importantly, discuss with your children what they can and can’t do on the computer. Children use computers to play educational games, chat with friends and even read textbooks for school. Computers are incredible tools for conducting research and learning new information. However, they can also be used to harm others. </p>

<p>Talk with your kids about the dangers of connecting with strangers online. Talk candidly about the reality that everything they do or say on social networking sites can be kept and used against them later in life. Discuss how what they do and say online can either help or hurt others. Talk about the types of websites you do and don’t want them visiting. </p>

<p>If your children are preschool-age, stay with them while they use the computer to prevent them from stumbling across content that might frighten them. Limit their usage to educational games.</p>

<p>Make sure your children know you’re watching them. Talk often with your children about what you notice, and ask questions about what they do and who they interact with online. Remember that keeping an open line of communication builds trust. It shows your children that your goal is not to restrict their freedom, but to protect and prepare them for adulthood. </p>
