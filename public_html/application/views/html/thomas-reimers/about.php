			<p>Thomas M. Reimers, Ph.D., is Director of Boys Town's Behavioral Health Clinic, which provides outpatient services and counseling to youth and families. He is a licensed psychologist and is a Clinical Associate Professor in Pediatrics at the Creighton University School of Medicine.</p>
			
			<p>Dr. Reimers began his career at The John Hopkins School of Medicine, where he completed his Post-doctoral Fellowship in Behavior Pediatrics in 1987. After various research and psychology positions, Dr. Reimers was named Co-Director of Children’s Hospital’s Feeding and Growth Management Program. He provided outpatient services to children presenting severe behavioral difficulties for nearly 20 years before joining the staff at Boys Town.</p>
			
			<p>Dr. Reimers is a member of the Association for Behavior Analysis and Nebraska Psychological Association, and has authored nearly 30 papers on child and adolescent behavior. He has traveled across the country to give presentations and workshops on his research and findings.</p>
			
